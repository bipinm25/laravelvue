    public function saveCategory(Request $request)
    {               
        
        $category = new Category();
        if(isset($request->id) &&  $request->id > 0){
            $category = Category::findOrFail($request->id);
        }   
        $category->category = $request->fieldvalue;
        $category->save();

    }

    public function getCategory(){
       
